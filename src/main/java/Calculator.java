public class Calculator {

    public int addNumbers(int number_1, int number_2) {
         return number_1 + number_2;
    }

    public int subtractNumbers(int number_1, int number_2) {
        return number_1 - number_2;
    }

    public int multiplyNumbers(int number_1, int number_2) {
        return number_1 * number_2;
    }

    public int divideNumbers(int number_1, int number_2) {
        return number_1 / number_2;
    }


}

